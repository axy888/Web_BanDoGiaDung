                    <p>“DOVODUNG” cam kết bảo mật an toàn tuyệt đối với thông tin cá nhân của khách hàng theo chính sách bảo vệ thông tin của doanh nghiệp và các quy định của pháp luật Việt Nam. Cụ thể :</p>
                   
                    <h3>1. Mục đích thu thập thông tin cá nhân</h3>

                    <p>- Việc thu thập dữ liệu trên bao gồm: email, điện thoại, tên đăng nhập, mật khẩu đăng nhập, địa chỉ khách hàng (thành viên). Đây là các thông tin cần thành viên cung cấp bắt buộc khi đăng ký sử dụng dịch vụ và để liên hệ xác nhận khi khách hàng đăng ký sử dụng dịch vụ trên ứng dụng nhằm đảm bảo quyền lợi cho cho người tiêu dùng.</p>
                    <p>- Các thành viên sẽ tự chịu trách nhiệm về bảo mật và lưu giữ mọi hoạt động sử dụng dịch vụ dưới tên đăng ký, mật khẩu và hộp thư điện tử của mình. Ngoài ra, thành viên có trách nhiệm thông báo kịp thời cho Chúng tôi về những hành vi sử dụng trái phép, lạm dụng, vi phạm bảo mật, lưu giữ tên đăng ký và mật khẩu của bên thứ ba để có biện pháp giải quyết phù hợp.</p>
                    <p>- Ứng dụng có thể yêu cầu quyền truy cập Thư viện/ Bộ nhớ để giúp người dùng chọn, lưu ảnh về thiết bị.</p>
                    <p>- Ứng dụng có thể yêu cầu quyền truy cập Vị trí để đưa ra các kết quả hợp lý nhất với người dùng. Chúng tôi không chia sẻ, lưu trữ thông tin vị trí của người dùng.</p>
                    <p>- Dữ liệu các đoạn hội thoại, ảnh tải lên của người dùng sẽ được đẩy lên hệ thống của chúng tôi. Chúng tôi chỉ lưu trữ, không chia sẻ các dữ liệu hội thoại, ảnh tải lên nhằm mục đích giúp người dùng có thể xem lại nội dung đã đăng của mình.</p>
                    <h3>2. Phạm vi thu thập thông tin</h3>
                    <p>"DOVODUNG" sử dụng thông tin thành viên cung cấp để:</p>
                    <p>- Cung cấp các dịch vụ đến Thành viên;</p>
                    <p>- Gửi các thông báo về các hoạt động trao đổi thông tin giữa thành viên và người quản trị.</p>
                    <p>- Ngăn ngừa các hoạt động phá hủy tài khoản người dùng của thành viên hoặc các hoạt động giả mạo Thành viên;</p>
                    <p>- Trong trường hợp có yêu cầu của pháp luật: Chúng tôi có trách nhiệm hợp tác cung cấp thông tin cá nhân thành viên khi có yêu cầu từ cơ quan tư pháp bao gồm: Viện kiểm sát, tòa án, cơ quan công an điều tra liên quan đến hành vi vi phạm pháp luật nào đó của khách hàng. Ngoài ra, không ai có quyền xâm phạm vào thông tin cá nhân của thành viên.</p>
                    <h3>3. Thời gian lưu trữ thông tin</h3>
                    <p>Dữ liệu cá nhân của Thành viên sẽ được lưu trữ cho đến khi có yêu cầu hủy bỏ hoặc tự thành viên đăng nhập và thực hiện hủy bỏ. Còn lại trong mọi trường hợp thông tin cá nhân thành viên sẽ được bảo mật trên máy chủ của chúng tôi.</p>
                    <h3> 4. Những người hoặc tổ chức có thể được tiếp cận với thông tin đó</h3>
                    <p>– Chúng tôi sử dụng dịch vụ từ một nhà cung cấp dịch vụ là bên thứ ba để thực hiện một số hoạt động liên quan. Khi đó, bên thứ ba có thể truy cập hoặc xử lý các thông tin cá nhân trong quá trình cung cấp các dịch vụ đó. Chúng tôi yêu cầu các bên thứ ba này tuân thủ mọi luật lệ về bảo vệ thông tin cá nhân liên quan và các yêu cầu về an ninh liên quan đến thông tin cá nhân.</p>
                    <p>– Chuyển giao kinh doanh (nếu có): trong trường hợp sáp nhập, hợp nhất toàn bộ hoặc một phần với công ty khác, người mua sẽ có quyền truy cập thông tin được chúng tôi lưu trữ, duy trì trong đó bao gồm cả thông tin cá nhân</p>
