                    <h2>Quy trình giao dịch</h2>
                    <p>Hoạt động giao dịch mua bán hàng hóa trên ứng dụng Inochi được thực hiện dựa trên nguyên tắc tự do, tự nguyện, bình đẳng, tông trọng quyền và lợi ích hợp pháp của các bên tham gia, không vi phạm với quy định của Pháp luật và quy định của ứng dụng Inochi.</p>
                    <p>Mọi sản phẩm hàng hóa/ dịch vụ giao dịch trên website và ứng dụng đáp ứng đầy đủ các điều kiện theo quy định của công ty và theo quy định có liên quan của Pháp luật, không thuộc trường hợp các mặt hàng cấm giao dịch, hàng lậu, hàng giả.</p>
                    <p>Đối tác và người sử dụng khi đăng ký tài khoản và sử dụng dịch vụ trên ứng dụng Inochi đã đồng ý với các điều khoản hoạt động, chịu trách nhiệm pháp lý với hành vi của mình và cam kết thực hiện mọi quy định liên quan.</p>
                    <p>Công ty cổ phần Tân Phú Việt Nam cam kết xây dựng quy chế dựa trên các quy định về thương mại điện tử và tuân theo các quy định của Pháp luật Việt Nam.</p>
                    <h3>1 Quy trình dành cho người mua hàng/sử dụng dịch vụ:</h3>
                    <p>Bước 1: Truy cập vào ứng dụng Inochi ( bao gồm website Inochi hoặc ứng dụng Inochi trên điện thoại Android/IOS) để tìm hiểu thông tin về các vấn đề liên quan đến nhu cầu của mình nhằm xác định chính xác vấn đề, giá thành tham khảo.</p>
                    <p>Bước 2:Lựa chọn Nhóm sản phẩm sau đó tiếp tục chọn sản phẩm mà mình quan tâm.</p>
                    <p>Bước 3:Điền các thông tin liên hệ, bao gồm Số điện thoại, địa chỉ nhận hàng và thanh toán</p>
                    <p>Bước 4:Chờ hệ thống kết nối.</p>
                    <p>Bước 5:Khi nhà cung cấp sản phẩm hoàn thành cung cấp sản phẩm, khách hàng thanh toán cho nhà cung cấp sản phẩm bằng tiền mặt, chuyển khoản hoặc các hình thức thanh toán khác do hai bên thỏa thuận. Khi thanh toán xong, nhà cung cấp xác nhận hệ thống Inochi dịch vụ đã hoàn thành. Hệ thống sẽ thông báo chi tiết hóa đơnvà thông tin đến khách hàng.</p>
                    <h3>2 Quy trình giao nhận vận chuyển:</h3>
                    <p>Bước 1:
                    Khi khách hàng đã hoàn thành các bước chọn sản phẩm sẽ chọn đối tác vận chuyển.</p>
                    <p>Bước 2: Hàng hóa sẽ được đóng gói từ kho hàng của Inochi và chuyển tới đối tác đơn vị vận chuyển.</p>
                    <p>Bước 3:Đơn vị vận chuyển sẽ giao sản phẩm tới địa chỉ nhận sản phẩm của Khách hàng.</p>
                    <h3>Chính sách kiểm hàng:</h3>
                    <p>Để quyền lợi của khách hàng được bảo vệ tối đa khi tham gia mua sắm trên App INOCHI. Chúng tôi có chính sách hỗ trợ khách hàng được kiểm tra, xem hàng, đồng kiểm với nhân viên giao hàng khi nhận hàng. Nghĩa là, khi bạn nhận được gói hàng từ nhân viên giao hàng, bạn được phép mở ra để kiểm tra ngoại quan về sản phẩm trước khi thanh toán.</p>
                    <h3>3 Quy trình xác nhận/hủy đơn hàng:</h3>
                    <p>Khi kết nối với nhà cung cấp dịch vụ, nhà cung cấp sẽ nêu phương án cung cấp dịch vụ, giá thành dự toán. Nếu khách hàng không đồng ý với phương án trên, khách hàng có thể hủy đơn hàng.</p>
                    <h3>4 Quy trình đổi trả hàng và hoàn tiền:</h3>
                    <p>Khi sản phẩm có lỗi của nhà sản xuất ( có bằng chứng chứng minh không phải lỗi do đơn vị vận chuyển và khách hàng ) Khách hàng sẽ liên hệ với nhà cung cấp sản phẩm để được đổi trả hàng và hoàn tiền.</p>
